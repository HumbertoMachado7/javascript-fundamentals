// operadores aritmeticos
3+2 // 5
50-10 // 40
10*20 // 200
20/2 // 10

// contatenacion
"Dario" + "Echazu" // 'DarioEchazu'

// de negacion
!true // false
!false // true

// asignacion
var a = 1; // a le asigno valor 1

// comparacion
3 == "3"; // true
3 === "3" // false

// comparacion de mayor o menor. Mayor igual o menor igual.
4 < 7; // true
4 > 7; // false
8 <= 9 // true
8 >= 9 // false

// validacion de valores and, or
a && b; // si uno de los valores es false, resultado false.
a || b; // si uno de los valores es true, resultado es true.

// valor para incrementar
var edad = 30;
edad++; // aumenta 1
edad; // 31
edad++; // ahora se incrementa otro mas
edad; // 32
edad++; // ahora es 32

// incremento con otro valores al 1
edad += 2; // incremento dos mas al valor de la edad
edad; // 34

// valor para decrimentar
var edad = 30;
edad--; // restamos 1 
edad; // 29
edad--; // restamos 1
edad; // 28

// decremento con otro valor distinto a 1
edad -= 2; // resto 2 al valor de edad
edad; // 26